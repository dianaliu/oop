public class xxMath{
	public static void main(String[] args){
		int i = 0;
		double j = 0;
		float k = 0;
		
		System.out.println("i: " + i);
		System.out.println("j: " + j);
		System.out.println("k: " + k);
		
		i = 50;
		System.out.println("i +50: " + i);
		i = i - 10;
		System.out.println("i -10: " + i);
		i = i * 10;
		System.out.println("i *10: " + i);
		i = i / 10;
		System.out.println("i /10: " + i);
	}
}
